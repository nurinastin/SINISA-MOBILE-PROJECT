package com.project.sinisa.penyuluhan.model;

public class Penyuluhan_Model {
    String kode, nama, nama_instansi, status, tanggal_input, tanggal_output, materi;

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNama_instansi() {
        return nama_instansi;
    }

    public void setNama_instansi(String nama_instansi) {
        this.nama_instansi = nama_instansi;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTanggal_input() {
        return tanggal_input;
    }

    public void setTanggal_input(String tanggal_input) {
        this.tanggal_input = tanggal_input;
    }

    public String getTanggal_output() {
        return tanggal_output;
    }

    public void setTanggal_output(String tanggal_output) {
        this.tanggal_output = tanggal_output;
    }

    public String getMateri() {
        return materi;
    }

    public void setMateri(String materi) {
        this.materi = materi;
    }
}
